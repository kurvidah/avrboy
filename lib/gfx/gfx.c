void gfx_draw_string(uint8_t x, uint8_t y, const char *s, uint8_t value) {
    while (*s) {
        uint8_t c = (uint8_t)*s++;
        if (c >= 0x20 && c <= 0x7E) {
            uint8_t idx = c - 0x20;
            for (uint8_t col = 0; col < 5; col++) {
                uint8_t bits = pgm_read_byte(&font5x7[idx][col]);
                for (uint8_t row = 0; row < 7; row++) {
                    if (bits & (1 << row)) lcd_set_pixel(x + col, y + row, value);
                }
            }
        }
        x += 6;
    }
}
